// Crear un array con un solo elemento
let frutas = ["manzana"];

// Mostrar el estado del array antes de usar push
console.log("Antes de push:", frutas);

// Usar push para añadir "naranja" al array
frutas.push("naranja");

// Mostrar el estado del array después de usar push
console.log("Después de push:", frutas);

// Usar pop para eliminar el último elemento del array
frutas.pop();

// Mostrar el estado del array después de usar pop
console.log("Después de pop:", frutas);
