// Declarar los valores
let numero = 42;
let texto = "JavaScript";
let booleano = true;

// Usar typeof para determinar y mostrar el tipo de dato
console.log("El tipo de dato de 42 es:", typeof numero);      // number
console.log("El tipo de dato de 'JavaScript' es:", typeof texto);  // string
console.log("El tipo de dato de true es:", typeof booleano);    // boolean
